package org.example;

public class LaskePintaAlat {
    public static double laskeYmpyra(double sade) {
        return 2 * Math.PI * sade;
    }
    public static double laskeNelio(double sivu) {
        return sivu * sivu;
    }
    public static double laskeSuorakulmainenKolmio(double kanta, double korkeus) {
        return 0.5 * kanta * korkeus;
    }
}
